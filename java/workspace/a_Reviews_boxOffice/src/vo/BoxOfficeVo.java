package vo;

public class BoxOfficeVo {
	private int ranking;
	private String filmName;
	private String releaseDate;
	private long income;
	private int watchCnt;
	private int screenCnt;
	
	public BoxOfficeVo() {;}
	
	
	public int getRanking() {
		return ranking;
	}
	public void setRanking(int ranking) {
		this.ranking = ranking;
	}
	public String getFilmName() {
		return filmName;
	}
	public void setFilmName(String filmName) {
		this.filmName = filmName;
	}
	public String getReleaseDate() {
		return releaseDate;
	}
	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}
	public long getIncome() {
		return income;
	}
	public void setIncome(long income) {
		this.income = income;
	}
	public int getWatchCnt() {
		return watchCnt;
	}
	public void setWatchCnt(int watchCnt) {
		this.watchCnt = watchCnt;
	}
	public int getScreenCnt() {
		return screenCnt;
	}
	public void setScreenCnt(int screenCnt) {
		this.screenCnt = screenCnt;
	}
	
	
	
	
	
	
	
}
